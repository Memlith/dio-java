public class KeywordsPrimitivos {
  public static void main(String[] args) {
    byte varByte = (byte) 1; // de -128 a 127 | 8 bits
    short varShort = (short) 2; // de -32768 a 32767 | 16 bits
    int varInt = 3; // de -2.147.483.648 a 2.147.483.647 | 32 bits
    long varLong = 4l; // de -2^63 a 2^63-1 | 64 bits
    float varFloat = 5; // de -14.4024E-37 a 3.40282347E+38 | 32 bits
    double varDouble = 6; // de -4.94E-307 a 1.79E+308 | 64 bits
    char varChar1 = 'a'; // de 0 a 65535 | 16 bits
    char varChar2 = 8; // de 0 a 65535 | 16 bits
    boolean varBool = true; // false or true | 1 bit
  }
}
